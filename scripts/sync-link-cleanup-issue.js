#!/usr/bin/env node

/**
 * Render a Check Links report into a GitHub issue body and either create
 * a new "auto-link-cleanup" issue or update the most recent open one.
 *
 * Reads the issues array written by scripts/check-links.js (path comes from
 * env LINK_ISSUES_FILE, defaults to /tmp/link-issues.json). Exits cleanly
 * if there are no issues to report.
 *
 * Requires: gh CLI authenticated; repo permissions issues: write.
 */

const fs = require('fs');
const { execFileSync } = require('child_process');

const LABEL = 'auto-link-cleanup';
const LABEL_COLOR = 'fbca04';
const LABEL_DESC = 'Automated repository link cleanup queue';
const ISSUE_FILE = process.env.LINK_ISSUES_FILE || '/tmp/link-issues.json';

function gh(args, opts = {}) {
	return execFileSync('gh', args, { encoding: 'utf-8', stdio: opts.stdio ?? ['ignore', 'pipe', 'inherit'] });
}

function ensureLabel() {
	try {
		gh(['label', 'create', LABEL, '--color', LABEL_COLOR, '--description', LABEL_DESC], { stdio: ['ignore', 'pipe', 'pipe'] });
	} catch {
		// label already exists -- idempotent
	}
}

function findOpenIssueNumber() {
	const out = gh(['issue', 'list', '--label', LABEL, '--state', 'open', '--limit', '5', '--json', 'number,createdAt']);
	const list = JSON.parse(out || '[]');
	if (list.length === 0) return null;
	list.sort((a, b) => (a.createdAt < b.createdAt ? 1 : -1));
	return list[0].number;
}

function renderBody(issues, runUrl) {
	const failures = issues.filter(i => ['not_found', 'archived', 'disabled'].includes(i.status));
	const redirects = issues.filter(i => i.status === 'redirected');
	const errors = issues.filter(i => i.status === 'error');

	const lines = [];
	lines.push('Check Links found ' + issues.length + ' repository link(s) that need attention.');
	lines.push('');
	lines.push('@cursoragent please open a PR that:');
	lines.push('1. for each **redirected** entry: update `data/projects.json` so its `repo` field points at the new location (and update `maintainer` if the owning org changed);');
	lines.push('2. for each **failure** (404 / archived / disabled): remove the entry from `data/projects.json`, unless it is a clear case of being replaced by another already-listed project, in which case just remove the duplicate;');
	lines.push('3. regenerate the README via `node scripts/generate-readme.js`;');
	lines.push('4. verify with `node scripts/check-links.js` that 0 failures remain (redirect warnings are tolerable but prefer to clean them too).');
	lines.push('');
	if (failures.length > 0) {
		lines.push('## Failures (remove or replace)');
		lines.push('');
		for (const i of failures) {
			lines.push('- [' + i.status + '] **' + i.name + '** (`' + i.repo + '`)' + (i.error ? ' -- ' + i.error : ''));
		}
		lines.push('');
	}
	if (redirects.length > 0) {
		lines.push('## Redirected (rewrite repo field)');
		lines.push('');
		for (const i of redirects) {
			lines.push('- **' + i.name + '**: `' + i.repo + '` -> `' + i.target + '`');
		}
		lines.push('');
	}
	if (errors.length > 0) {
		lines.push('## API errors (transient, will be re-checked next run)');
		lines.push('');
		for (const i of errors) {
			lines.push('- **' + i.name + '** (`' + i.repo + '`) -- ' + (i.error || 'unknown'));
		}
		lines.push('');
	}
	lines.push('---');
	lines.push('');
	lines.push('Generated by `Check Links` workflow on ' + new Date().toISOString().slice(0, 10) + (runUrl ? ' ([run](' + runUrl + '))' : '') + '. This issue is automatically refreshed on every schedule run; close it once the cleanup PR is merged.');
	return lines.join('\n');
}

function main() {
	if (!fs.existsSync(ISSUE_FILE)) {
		console.log('No issues file at ' + ISSUE_FILE + ', nothing to do.');
		return;
	}
	const issues = JSON.parse(fs.readFileSync(ISSUE_FILE, 'utf-8'));
	if (!Array.isArray(issues) || issues.length === 0) {
		console.log('No link issues, skipping.');
		return;
	}

	const runUrl = process.env.GITHUB_SERVER_URL && process.env.GITHUB_REPOSITORY && process.env.GITHUB_RUN_ID
		? `${process.env.GITHUB_SERVER_URL}/${process.env.GITHUB_REPOSITORY}/actions/runs/${process.env.GITHUB_RUN_ID}`
		: null;
	const body = renderBody(issues, runUrl);
	const month = new Date().toISOString().slice(0, 7);
	const title = `[auto] Repository link cleanup needed (${month})`;

	if (process.env.LINK_ISSUE_DRY_RUN === '1') {
		console.log('DRY RUN -- would create/update issue:');
		console.log('Title: ' + title);
		console.log('Body:');
		console.log(body);
		return;
	}

	ensureLabel();

	const existing = findOpenIssueNumber();
	if (existing) {
		console.log('Updating existing issue #' + existing);
		execFileSync('gh', ['issue', 'edit', String(existing), '--title', title, '--body', body], { stdio: 'inherit' });
		execFileSync('gh', ['issue', 'comment', String(existing), '--body', 'Refreshed by Check Links run on ' + new Date().toISOString().slice(0, 10) + '.'], { stdio: 'inherit' });
	} else {
		console.log('Creating new issue.');
		execFileSync('gh', ['issue', 'create', '--title', title, '--label', LABEL, '--body', body], { stdio: 'inherit' });
	}
}

main();
